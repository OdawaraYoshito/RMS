<?php

/*
|--------------------------------------------------------------------------
| テストケースの設定
|--------------------------------------------------------------------------
|
| テスト関数に渡されるクロージャはデフォルトで "PHPUnit\Framework\TestCase" クラスにバインドされます。
| 必要に応じて "pest()" 関数を使って異なるクラスやトレイトをバインドすることも可能です。
|
*/

pest()->extend(Tests\TestCase::class)
    ->use(Illuminate\Foundation\Testing\RefreshDatabase::class) // テスト実行時にデータベースをリフレッシュ
    ->in('Feature'); // Featureテストを対象に適用

/*
|--------------------------------------------------------------------------
| カスタム期待値（Expectations）
|--------------------------------------------------------------------------
|
| "expect()" 関数を使用して、特定の条件をチェックするカスタム期待値を定義できます。
| ここでは "toBeOne" メソッドを追加し、値が1であることを確認できるようにしています。
|
*/

expect()->extend('toBeOne', function () {
    return $this->toBe(1);
});

